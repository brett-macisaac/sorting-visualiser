
package Utils;

/*
     * Given that Java doesn't support operator overloading, it might be useful to have an enumeration whose values
       represent common comparison operators.
*/
public enum CompareEnum
{
    E,  // Equals (==)
    NE, // Not Equals (!=)
    G,  // Greater (>)
    L,  // Less than (<)
    GE, // Greater or Equal (>=)
    LE  // Less than or Equal (<=)
}
